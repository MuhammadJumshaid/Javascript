// Break Keyword (Break key word stops the execution and comes outside of the code)


for(let i = 0 ; i < 10 ; i++){
    if(i == 4){
        break;
    }
    console.log(i);
}
console.log("oustside of the loop");



// Continue Keyword 
//When code reach to continue , it comes again at loop codition 


for(let i = 0 ; i <= 10 ; i ++){
    if(i == 4){
        continue;
        
    }
    console.log(i);
}

console.log("outside the loop");




