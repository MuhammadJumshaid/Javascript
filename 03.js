// Rules for nameing variables 
 
// You cannot start variable name with number 
//
//example 
// 1value(invalid)
// value1(valid)
//
//
//
//
// You can only use underscore  _ or $ dollar sign symbol
//
// first_name (valid)
  // _firstname (valid)
//
//
//  first$name (valid)
//  $firstname(valid)
//
//
//  you cannot use spaces 
//
//  first name (invalid)
// 
//
//  Conventions
// Start with small letter and use camel case 
